package quiz06;

public class BreadAndChange {

	// field
	private int bread;
	private int change;

	// cunstructor
	public BreadAndChange(int bread, int change) {
		this.bread = bread;
		this.change = change;
	}
	
	// getter
	public int getBread() {
		return bread;
	}
	
	public int getChange() {
		return change;
	}
	
}
